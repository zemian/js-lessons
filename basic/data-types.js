// Variable or sometimes called binding
let greeting = "Hello World!";
console.log(greeting);

// Variable types: number, string, boolean
let n = 987;
console.log(n);
let pi = 3.14;
console.log(pi);
let name = "javascript";
console.log(name);
let flag = true;
console.log(flag);

// Undefined and NULL
name = null;
console.log(name);
flag = undefined;
console.log(flag);

// Declaring variavble with "", "var" and "const"
foo = "foo";
console.log(foo);
var bar = "bar";
console.log(bar);
const baz = "baz";
console.log(baz);

// Other speical number value
let num = NaN;
console.log(num);

// Declare multiple variables on same line
let a = 9, b = 8;
console.log(a);
console.log(b);

let [c, d] = ["foo", "bar"];
console.log(c);
console.log(d);

// String special chars escaping
let question = "What's your name?";
console.log(question);
let answer = 'My name is "JavaScript"';
console.log(answer);
console.log('What\'s your name again?');
console.log("I said my name is \"JavaScript\"!");

console.log("I can\ncreate new line\nwith special chars.\n\n");
